          <h3 id="87a705f0-2ff6-45e2-8d4a-63255a0b7206" class="">4. 개인정보의 처리의 위탁</h3>
          <p id="aaaf5a44-4c55-4502-974a-930354bfa086" class="">(1) 회사는 서비스 향상을 위해서 이용자의 개인정보를 외부 전문업체에 위탁하여 처리할 수 있습니다.
            개인정보의 처리를 위탁하는 경우에는 미리 그 사실을 이용자에게 공지하여 이용자의 동의를 받을 것입니다.</p>
          <p id="c8904a14-aa1d-4384-9505-d70c4b57911c" class="">(2) 이용자의 개인정보의 위탁처리하는 경우에는 위탁계약(위탁업무 수행 목적 외 개인정보 처리 금지,
            개인정보의 기술적 관리적 보호조치, 위탁업무의 목적 및 범위, 재위탁 제한, 개인정보에 대한 접근 제한 등 안정성 확보 조치, 위탁업무와 관련하여 보유하고 있는 개인정보의 관리 현황 점검 등 감독,
            수탁자가 준수하여야 할 의무를 위반한 경우의 손해배상 등이 내용이 포함됨)을 통하여 서비스제공자의 개인정보보호 관련 지시엄수, 개인정보에 관한 비밀유지, 제3자 제공의 금지 및 사고시의 책임부담 등을
            명확히 규정하고 당해 계약내용을 서면 또는 전자적으로 보관할 것입니다.</p>
          <p id="eea227c0-49aa-4831-9012-fcfc6298a9a3" class="">(3) 이 경우에 회사는 위탁업무의 내용, 개인정보 처리업무를 위탁받아 처리하는 자(수탁자)를 이용자이
            언제든지 쉽게 확인할 수 있도록 회사의 인터넷홈페이지에 지속적으로 게재하여 공개합니다.</p>
          <p id="0647d4b8-bac8-4355-ba3b-8b61f168fd51" class="">(4) 회사는 업무 위탁으로 인하여 이용자의 개인정보가 분실, 도난, 유출, 변조, 훼손되지 아니하도록
            수탁자를 교육하고 수탁자가 개인정보를 안전하게 처리하는 지를 감독할 것입니다.</p>
