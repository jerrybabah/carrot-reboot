          <h3 id="59585deb-cfeb-4a65-884c-d3c653d9d6a2" class="">6. 개인정보 파기절차 및 방법</h3>
          <p id="aa6828d0-5b82-4dc4-bf07-a65860fc5234" class="">이용자의 개인정보는 원칙적으로 개인정보의 수집 및 이용목적이 달성되면 지체 없이 파기합니다. 회사의 개인정보
            파기절차 및 방법은 다음과 같습니다.</p>
          <p id="eb49bf5a-3772-43d0-8636-79da4caffa27" class="">(1) 파기절차</p>
          <ul id="907462a2-9b90-4e74-b191-8b5b73779835" class="bulleted-list">
            <li style="list-style-type:disc">회사는 개인정보의 파기에 관한 사항을 기록하고 관리하며, 파기는 개인정보 보호책임자의 책임하에 수행되며, 개인정보 보호책임자는 파기 결과를
              확인합니다.</li>
          </ul>
          <ul id="f66c5d94-ca72-41e9-941c-178e15938982" class="bulleted-list">
            <li style="list-style-type:disc">회사는 다른 법령에 따라 보존해야 하는 경우에는 예외적으로 이용자의 개인정보를 파기하지 않을 수 있습니다.</li>
          </ul>
          <p id="0c7623a0-eb82-4542-9018-385ce772a28b" class="">(2) 파기방법</p>
          <ul id="01b9ea13-92c8-4506-a263-281486e38238" class="bulleted-list">
            <li style="list-style-type:disc">종이에 출력된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기합니다.</li>
          </ul>
          <ul id="26d258cd-5a74-455b-8d93-6689bc59faea" class="bulleted-list">
            <li style="list-style-type:disc">전자적 파일 형태로 저장된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제합니다.</li>
          </ul>
          <p id="87a3e2f1-2e45-454f-b1e1-2278f3b9ef77" class="">(3) 미파기 정보의 보존방법회사는 법령에 따라 개인정보를 파기하지 않고 보존하는 경우에 해당 개인정보 또는
            개인정보파일을 다른 개인정보와 분리하여 저장 관리합니다.</p>
          <h3 id="e5af7fdb-d946-4a0f-a2e6-81303d0325ff" class="">7. 이용자 및 법정대리인의 권리와 그 행사방법</h3>
          <p id="3f60f3e6-674c-4d31-aac7-acd41ad6c8f0" class="">이용자의 개인정보는 원칙적으로 개인정보의 수집 및 이용목적이 달성되면 지체 없이 파기합니다. 회사의 개인정보
            파기절차 및 방법은 다음과 같습니다.</p>
          <p id="1f8f47ff-f96e-4f42-b710-3b38fa386063" class="">(1) 이용자 및 법정대리인은 언제든지 회사에 &quot;김프로 서비스&quot;를 통하여 등록되어 있는
            자신의 개인정보, 회사가 이용자의 개인정보를 이용하거나 제3자에게 제공한 현황, 회사에게 개인정보 수집, 이용, 제공 등의 동의를 한 현황에 대하여 열람이나 제공을 요구할 수 있고, 오류가 있는
            경우에는 그 정정을 요구할 수 있으며, 삭제 내지 가입해지를 요구할 수도 있습니다.</p>
          <p id="8fbe2fb0-3045-48e8-97b5-b223ba3d7ca3" class="">(2) 이 경우에 회사는 지체 없이 그 개인정보를 조사하여 이용자의 요구에 따라 정정, 삭제 등 필요한
            조치를 한 후 그 결과를 이용자에게 알립니다. 회사는 필요한 조치를 할 때까지는 해당 개인정보를 이용하거나 제공하지 않습니다.</p>
          <p id="a1cad811-bf6d-40da-a1f8-0dffa3aec6d0" class="">(3) 이용자는 언제든지 회사에 자신의 개인정보 처리의 정지를 요구할 수 있으며, 이 경우에 회사는 지체
            없이 그 요구에 따라 개인정보 처리의 전부 내지 일부를 정지하며, 처리가 정지된 개인정보에 대하여 지체 없이 그 개인정보의 파기 등 필요한 조치를 취합니다.</p>
          <p id="b20281eb-71d5-4d8c-96d3-edfb1b16142f" class="">(4) 개인정보와 관련하여 의의나 의견이 있으신 분은 개인정보 보호책임자 및 담당자에게 서면, 전화 또는
            E-mail로 연락하시면 접수 즉시 처리하고 결과를 안내해 드리겠습니다. 로그온(log-on)한 상태에서는 주위의 다른 사람에게 개인정보가 유출되지 않도록 특별히 주의를 기울이시기 바랍니다.</p>
          <h3 id="cb94296e-7327-4099-a851-87a3731123a8" class="">8. 개인정보 자동 수집 장치의 설치/운영 및 거부에 관한 사항</h3>
          <p id="834f73ed-a7e3-4705-8cbd-8eee6b7dc26f" class="">(1) 회사는 계정정보를 생성하기 위해 이용자가 회사가 제공하는 서비스의 프로그램을 실행시 기기식별번호
            (디바이스 아이디 또는 IMEI)를 자동으로 수집하게 됩니다. 이용자가 기기식별번호를 자동으로 수집하는 것을 거부하는 경우 회사의 서비스를 이용할 수 없습니다.</p>
          <p id="7cdd9a28-ae61-46e2-b1e6-86af8b2a689e" class="">(2) 회사는 이용자들에게 특화된 맞춤서비스를 제공하기 위해서 이용자들의 정보를 저장하고 수시로 불러오는
            &#x27;쿠키(cookie)&#x27;를 사용합니다. 쿠키는 웹사이트를 운영하는데 이용되는 서버(HTTP)가 이용자의 컴퓨터 브라우저에게 보내는 소량의 정보이며 이용자들의 PC 컴퓨터내의 하드디스크에
            저장되기도 합니다.</p>
          <ul id="71d4f940-e013-4838-b46f-86e2f506b70a" class="bulleted-list">
            <li style="list-style-type:disc">쿠키 등 사용 목적이용자들이 방문한 “김프로 서비스”의 각 서비스와 웹 사이트들에 대한 방문 및 이용형태, 인기 검색어, 보안접속 여부,
              이용자 규모 등을 파악하여 이용자에게 최적화된 정보 제공을 위하여 사용합니다.</li>
          </ul>
          <ul id="f0471ed8-7a5e-4418-b161-3e241691743e" class="bulleted-list">
