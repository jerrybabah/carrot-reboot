              저장할 때마다 확인을 거치거나, 모든 쿠키의 저장을 거부할 수 있습니다. </li>
          </ul>
          <p id="14f2fe3d-f449-4c6c-9d78-5db7ae7a0ce2" class="">설정방법 예(인터넷 익스플로어의 경우) : 웹 브라우저 상단의 도구 &gt; 인터넷 옵션 &gt; 개인정보
